package sensor;

public class WriteBackStorage {
	
	private float uv;
	private float color;
	
	public WriteBackStorage() {}
	
	public float getUV() {
		return this.uv;
	}
	
	public void setUV(float uv) {
		this.uv = uv;
	}
	
	public float getColor() {
		return this.color;
	}
	
	public void setColor(float color) {
		this.color = color;
	}
}
